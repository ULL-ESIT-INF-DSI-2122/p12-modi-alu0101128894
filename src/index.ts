export function add(firstnumber: number, secondnumber: number): number {
  return firstnumber + secondnumber;
}
console.log(`add(1,7): ${add(1, 8)}`);


export function resta(firstnumber: number, secondnumber: number): number {
  return firstnumber - secondnumber;
}
console.log(`resta(1,7): ${resta(1, 7)}`);


export function multiplicacion(firstnumber: number, secondnumber: number): number {
  return firstnumber * secondnumber;
}
console.log(`multiplicacion(1,7): ${multiplicacion(1, 7)}`);


export function division(firstnumber: number, secondnumber: number): number {
  return firstnumber / secondnumber;
}
console.log(`division(1,7): ${division(1, 7)}`);